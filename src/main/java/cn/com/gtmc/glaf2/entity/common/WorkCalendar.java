package cn.com.gtmc.glaf2.entity.common;

import java.util.Date;

import cn.com.gtmc.glaf2.simple.DefaultStringEntity;

public class WorkCalendar extends DefaultStringEntity {
    private static final long serialVersionUID = -6371095522005568801L;
    
    // 工作类型中的工作值
    public static final String WORK_TYPE_WORK = "W";
    
    // 工作类型中的休息值
    public static final String WORK_TYPE_FURLOUGH = "N";
    
    // 日期
    private Date day;
    
    // 类型
    private String workType;
    
    // 日期格式串
    private String dayStr;
    
    public Date getDay() {
        return day;
    }
    
    public void setDay(Date day) {
        this.day = day;
    }
    
    public String getWorkType() {
        return workType;
    }
    
    public void setWorkType(String workType) {
        this.workType = workType;
    }
    
    public String getDayStr() {
        return dayStr;
    }
    
    public void setDayStr(String dayStr) {
        this.dayStr = dayStr;
    }
}
