package cn.com.gtmc.glaf2.entity.common;

import cn.com.gtmc.glaf2.simple.DefaultIntegerEntity;

/**
 * @author  dream.li 
 * @version V1.0 
 * @Title Document
 * @Package cn.com.intasect.entity.common
 * @Description: 文档管理实体类
 * @date 2017年7月12日 下午2:39:41 
 */
public class Document extends DefaultIntegerEntity{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2344489856274509572L;

	public Document(){
		
	}
	
	private String referType;//关联类型
	private String referKey;//关联主键
	private String fileName;//文件名称
	private String fileType;//文件类型
	private String filePath;//保存在文档管理系统的文件路径
	private String fileUid;//保存在文档管理系统的文件id
	private String localPath;//文件路径 
	private Long fileSize;//文件大小
	private Integer permissionDown;//是否允许下载
	/**
	 * 权限类型
	 * 无权限控制：none
     * 允许指定人员访问：user
     * 允许指定部门访问：dept
     * 允许指定角色访问：role
     * 允许指定部门角色访问：deptAndRole
	 */
	private String permissionType;
	private String permissionUser;//权限人员
	private String permissionDept;//权限部门
	private String permissionRole;//权限角色
	
	public String getReferType() {
		return referType;
	}
	public void setReferType(String referType) {
		this.referType = referType;
	}
	public String getReferKey() {
		return referKey;
	}
	public void setReferKey(String referKey) {
		this.referKey = referKey;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	
	public Long getFileSize() {
		return fileSize;
	}
	public void setFileSize(Long fileSize) {
		this.fileSize = fileSize;
	}
	public Integer getPermissionDown() {
		return permissionDown;
	}
	public void setPermissionDown(Integer permissionDown) {
		this.permissionDown = permissionDown;
	}
	public String getPermissionType() {
		return permissionType;
	}
	public void setPermissionType(String permissionType) {
		this.permissionType = permissionType;
	}
	public String getPermissionUser() {
		return permissionUser;
	}
	public void setPermissionUser(String permissionUser) {
		this.permissionUser = permissionUser;
	}
	public String getPermissionDept() {
		return permissionDept;
	}
	public void setPermissionDept(String permissionDept) {
		this.permissionDept = permissionDept;
	}
	public String getPermissionRole() {
		return permissionRole;
	}
	public void setPermissionRole(String permissionRole) {
		this.permissionRole = permissionRole;
	}
	public String getFileUid() {
		return fileUid;
	}
	public void setFileUid(String fileUid) {
		this.fileUid = fileUid;
	}
	public String getLocalPath() {
		return localPath;
	}
	public void setLocalPath(String localPath) {
		this.localPath = localPath;
	}
	
}
